import dotenv from "dotenv";
dotenv.config();

function requireEnv(name: string): string {
  const value = process.env[name];
  if (!value) {
    throw new Error(`Missing required env var: ${name}`);
  }
  return value;
}

export const env = {
  PORT: requireEnv("PORT"),
  WEATHER_API_KEY: requireEnv("WEATHER_API_KEY"),
};